//homepage version of back to top button
$(document).ready(function() {
	$(window).scroll(function() {
		if($(this).scrollTop() > 200) {
			$('.backToTop').fadeIn();
		} else {
			$('.backToTop').hide();
		}
	});
	$('.backToTop').click(function() {
		$('html, body').animate({scrollTop: 0}, 300);
		return false;
	});
});
</script>

