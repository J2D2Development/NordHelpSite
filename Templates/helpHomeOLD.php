<script>
//homepage only function - item hover effect
$(document).ready(function() {
	if(window.innerWidth > 1024) {
		$('.helpHomeButtonWrapper').hover(function() {
			$(this).find('.helpHomeButton').hide();
			$(this).find('.helpHomeButtonHidden').fadeIn(500);
		}, function() {
			$(this).find('.helpHomeButtonHidden').hide();
			$(this).find('.helpHomeButton').show();
		});
	} else {
		$('.helpHomeButtonWrapper').hover(function() {
			$(this).find('.helpHomeButton').hide();
			$(this).find('.helpHomeButtonHidden').show();
		}, function() {
			$(this).find('.helpHomeButtonHidden').hide();
			$(this).find('.helpHomeButton').show();
		});		
	}

	$(window).scroll(function() {
		if($(this).scrollTop() > 200) {
			$('.backToTop').fadeIn();
		} else {
			$('.backToTop').hide();
		}
	});
	$('.backToTop').click(function() {
		$('html, body').animate({scrollTop: 0}, 300);
		return false;
	});
});
</script>

